void main() {
  //Operadores Aritmétivos

  int uno = 10;
  int dos = 3;

  print('Suma: ${uno + dos}');  // Suma: 13
  print('Resta: ${uno - dos}');  // Resta: 7
  print(uno * dos);  // Multiplicación: 30
  print(uno / dos);  // División: 3.333...
  print(uno ~/ dos); // División entera: 3
  print(uno % dos);  // Módulo: 1

}